<script>
    document.addEventListener("DOMContentLoaded", function() {
        // Sélectionner tous les boutons de fermeture des messages
        var closeButtons = document.querySelectorAll('[data-dismiss-target]');

        // Ajouter un écouteur d'événement à chaque bouton de fermeture
        closeButtons.forEach(function(button) {
            button.addEventListener('click', function() {
                // Récupérer l'identifiant de la cible de fermeture
                var targetId = button.getAttribute('data-dismiss-target');
                console.log(targetId)
                // Trouver l'élément avec l'ID de la cible et le masquer
                var targetElement = document.querySelector(targetId);
                if (targetElement) {
                    targetElement.style.display = 'none';
                }
            });
        });
    });
</script>
